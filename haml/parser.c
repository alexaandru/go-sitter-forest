      if (lookahead == '/') ADVANCE(17);
      if (lookahead == ':') ADVANCE(60);
      if (lookahead == '=') ADVANCE(35);
      if (('\t' <= lookahead && lookahead <= '\r') ||
          lookahead == ' ') ADVANCE(46);
      if (lookahead != 0) ADVANCE(51);
      END_STATE();
